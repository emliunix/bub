from __future__ import annotations
from typing import (
    TypeVar, Generic, Callable, Union, Any, overload, FrozenSet, runtime_checkable
)
from collections.abc import Generator
import enum
import re

__version__: str

T = TypeVar("T")
U = TypeVar("U")
# The library accepts str, bytes, or list (tokens)
Stream = Union[str, bytes, list[Any]]

# --- Result (made generic) ---

class Result(Generic[T]):
    status: bool
    index: int
    value: T
    furthest: int
    expected: FrozenSet[str]

    @staticmethod
    def success(index: int, value: T) -> Result[T]: ...
    @staticmethod
    def failure(index: int, expected: str) -> Result[Any]: ...
    def aggregate(self, other: Result[Any] | None) -> Result[T]: ...

# --- Parser (generic) ---

class Parser(Generic[T]):
    """
    Parser[T] produces a value of type T when successful.
    """
    def __init__(self, wrapped_fn: Callable[[Stream, int], Result[T]]) -> None: ...

    # Low-level: returns the raw Result wrapper
    def __call__(self, stream: Stream, index: int) -> Result[T]: ...

    # High-level: unwraps Result and returns T (or raises ParseError)
    def parse(self, stream: Stream) -> T: ...
    def parse_partial(self, stream: Stream) -> tuple[T, Stream]: ...

    # --- Combinators (properly typed) ---
    def bind(self, bind_fn: Callable[[T], Parser[U]]) -> Parser[U]: ...
    def map(self, map_function: Callable[[T], U]) -> Parser[U]: ...
    def combine(self, combine_fn: Callable[..., U]) -> Parser[U]: ...
    def combine_dict(self, combine_fn: Callable[..., U]) -> Parser[U]: ...
    def concat(self) -> Parser[str]: ...

    # --- Sequencing ---
    def then(self, other: Parser[U]) -> Parser[U]: ...
    def skip(self, other: Parser[Any]) -> Parser[T]: ...
    def result(self, value: U) -> Parser[U]: ...

    # --- Repetition (list-producing) ---
    def many(self) -> Parser[list[T]]: ...
    def times(self, min: int, max: int | None = None) -> Parser[list[T]]: ...
    def at_most(self, n: int) -> Parser[list[T]]: ...
    def at_least(self, n: int) -> Parser[list[T]]: ...
    def optional(self, default: U | None = None) -> Parser[T | U]: ...
    def until(
        self,
        other: Parser[Any],
        min: int = 0,
        max: float = ...,
        consume_other: bool = False
    ) -> Parser[list[T]]: ...
    def sep_by(
        self,
        sep: Parser[Any],
        *,
        min: int = 0,
        max: float = ...
    ) -> Parser[list[T]]: ...

    # --- Utilities ---
    def desc(self, description: str) -> Parser[T]: ...
    def mark(self) -> Parser[tuple[tuple[int, int], T, tuple[int, int]]]: ...
    def tag(self, name: str) -> Parser[tuple[str, T]]: ...
    def should_fail(self, description: str) -> Parser[None]: ...

    # --- Operators ---
    @overload
    def __or__(self, other: Parser[T]) -> Parser[T]: ...
    @overload
    def __or__(self, other: Parser[U]) -> Parser[T | U]: ...
    def __or__(self, other: Parser[Any]) -> Parser[Any]: ...

    def __add__(self, other: Parser[U]) -> Parser[list[T | U]]: ...
    def __mul__(self, other: int | range) -> Parser[list[T]]: ...
    def __rshift__(self, other: Parser[U]) -> Parser[U]: ...
    def __lshift__(self, other: Parser[Any]) -> Parser[T]: ...

    # Fallback: any methods not listed above are treated as Any
    def __getattr__(self, name: str) -> Any: ...

# --- Module-level constructors ---

def success(value: T) -> Parser[T]: ...
def fail(expected: str) -> Parser[Any]: ...

@overload
def alt(*parsers: Parser[T]) -> Parser[T]: ...
@overload
def alt(*parsers: Parser[Any]) -> Parser[Any]: ...

@overload
def seq(*parsers: Parser[T]) -> Parser[list[T]]: ...
@overload
def seq(**kw_parsers: Parser[T]) -> Parser[dict[str, T]]: ...

def generate(
    fn: Callable[..., Generator[Parser[Any], Any, T]]  # yields Parser, sends value, returns T
) -> Parser[T]: ...
def string(expected_string: str, transform: Callable[[str], str] = ...) -> Parser[str]: ...
def regex(
    exp: str | bytes | re.Pattern[str],
    flags: int = 0,
    group: int | str | tuple[int | str, ...] = 0
) -> Parser[str | tuple[str, ...]]: ...

# --- Predefined parsers ---
index: Parser[int]
