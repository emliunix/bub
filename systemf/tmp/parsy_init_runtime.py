eof: Parser[None]

def from_enum(enum_cls: type[enum.Enum], transform: Callable[[str], str] = ...) -> Parser[enum.Enum]: ...

class forward_declaration(Parser[Any]):
    def become(self, other: Parser[T]) -> None: ...

